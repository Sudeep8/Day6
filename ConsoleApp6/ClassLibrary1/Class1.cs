using System;

namespace Assembly1
{
    public class AssemblyClass
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }
        public int Dif(int a, int b)
        {
            return a - b;
        }
        public int Mul(int a, int b)
        {
            return a * b;
        }
        public int Div(int a, int b)
        {
            return a / b;
        }
    }
